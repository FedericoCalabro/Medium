// Receiver: Chef (the one who executes the commands)
class Chef {
  void preparePizza() {
    print("Chef is preparing a pizza...");
  }

  void cancelPizza() {
    print("Chef is cancelling the pizza order.");
  }

  void prepareBurger() {
    print("Chef is preparing a burger...");
  }

  void cancelBurger() {
    print("Chef is cancelling the burger order.");
  }
}
